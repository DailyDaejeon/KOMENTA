<template>
  <b-col>
    <div class="btn-cover">
      <button :disabled="pageNum === 0" @click="prevPage" class="page-btn">
        &lt;
      </button>
      <button
        :disabled="pageNum >= pageCount - 1"
        @click="nextPage"
        class="page-btn"
      >
        &gt;
      </button>
    </div>
    <ul class="stage clearfix">
      <li v-if="this.recentVODs.length == 0">
        <div>등록된 VOD가 없습니다.</div>
      </li>
      <li class="scene" v-for="vod in paginatedData" :key="vod.v_id" v-else>
        <div class="movie" onclick="return true">
          <div class="poster">
            <img :src="getPoster(vod.v_id)" height="100%" alt="" />
          </div>
          <div class="info" @click="goVod(vod.v_id)">
            <header>
              <h1>{{ vod.v_title }} {{ vod.ve_episode_num }}회</h1>
              <span class="year">{{ vod.ve_upload_date }}</span>
              <!-- <span class="rating">PG</span>
            <span class="duration">130 minutes</span> -->
            </header>
            <p>
              보러가기
            </p>
          </div>
        </div>
      </li>
    </ul>
  </b-col>
</template>

<script>
import { fetchRecentVOD } from '@/api/vod';
import { fetchAllVOD } from '@/api/user';
export default {
  data() {
    return {
      pageNum:0,
      allVODInfo:[],
      recentVODs: [],
    };
  },
  created() {
    this.getRecentVOD();
    this.getVODPoster();
  },
  props: {
    pageSize: {
      type: Number,
      required: false,
      default: 5
    }
  },
  methods: {
    //getRecentVOD api만들어야함
    async getRecentVOD() {
      const response = await fetchRecentVOD();
      this.recentVODs = response.data;
    },
    async getVODPoster(){
        const response = await fetchAllVOD();
        this.allVODInfo = response.data;
    },
    getPoster(index) {
        for (let i = 0; i < this.allVODInfo.length; i++) {
            if(this.allVODInfo[i].v_id === index) {
                let poster = this.allVODInfo[i].v_poster;
                return require(`@/assets/images/${poster}`);
            }
        }
    },
    goVod(vId) {
      this.$router.push(`/voddetail/${vId}`);
    },
    nextPage() {
      this.pageNum += 1;
    },
    prevPage() {
      this.pageNum -= 1;
    },
  },
  computed:{
    pageCount() {
        let listLeng = this.recentVODs.length,
        listSize = this.pageSize,
        page = Math.floor(listLeng / listSize);

        if(listLeng % listSize > 0) page += 1;

        return page;
    },
    paginatedData() {
        const start = this.pageNum * this.pageSize,
        end = start + this.pageSize;

        return this.recentVODs.slice(start, end);
        }
    }
};
</script>

